#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	while (n-- > 0 && (*s1 || *s2) && *((UC *)s1++) == *((UC *)s2++))
		;
	return ((UC) *(s1 - 1) - (UC) *(s2 - 1));
}
