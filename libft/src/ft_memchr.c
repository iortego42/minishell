#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	const unsigned char	*src;

	src = s;
	while (n-- > 0)
		if ((unsigned char)c == *(src++))
			return ((void *)(src - 1));
	return (NULL);
}
